	<p class="is-size-6-desktop">
		<strong>關於作品</strong>：《謙卑》（Humility: The Beauty of Holiness）是19世紀南非基督教領袖慕安德烈的著名作品之一，發表於1895年。“謙卑不是一種我們要帶到神面前的東西，也不是祂要賜下的東西；謙卑乃是意識到自己一無所有，且真正看見神是一切，遂讓路以便讓神真正成為一切。”這是本書作者為謙卑所下的定義。此書乃作者眾多著作中一顆閃閃發亮的寶貴珍珠，他的各種譯本為神的眾教會所喜愛，也曾使許多追求聖潔的聖徒們多次在那位柔和謙卑的神羔羊面前悔改、俯伏下來。<br>
		<strong>有聲出版及發行</strong>：磐石有聲事工（版權所有，請勿翻錄）
	</p>

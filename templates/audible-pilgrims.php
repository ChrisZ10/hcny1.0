			<p class="is-size-6">
				<strong>關於作品</strong>：《天路歷程》（The Pilgrim's Progress）是英國基督教作家、佈道家本仁約翰的著作，于1678年2月出版，是一部基督教的預言詩，後來也被認為是小說。它被認為是最重要的英國文學作品之一，被翻譯成200多種文字，暢銷百年。作者本仁約翰是位傑出的牧師，為神所重用。當年為了講道被捕下獄，這部偉大的著作就是在獄中完成的。本書描述基督徒為了到天城，中途經歷無數次危險，通過許多試驗，最後安抵天城的艱辛旅程。這是人生中不可錯過的一本書，因為它將改變你一生的命運。<br>

		<strong>關於作品</strong>：一個願意禱告的父母，是積極又喜樂的父母；不僅卸下自己的心頭重擔，更掌握教養孩子的成功之鑰。豈不知在你的仰望交託之中，神會啟示智慧，使教養子女這又大又難的事，化為切實可行的步驟，使孩子一天天更像主耶穌。作者劉梅蕾的丈夫、矽谷生命河靈糧堂主任牧師劉彤在介紹本書時說：“內人不但會教導禱告，她本身就是一位禱告的人（我是最好的見證人）。這些禱告文是她多年為兒女禱告的經驗累積，我可以大膽地說，本書絕對實用！絕對有效！所以讓我鼓勵你，從今開始，使用這本書一同上祝福兒女的道路吧！”<br>

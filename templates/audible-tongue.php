	<p class="is-size-6">
		<strong>關於作品</strong>：《神話語的權能》（The Tongue - A Creative Force）于1995年出版，作者是美國佈道家查爾斯凱普斯。話語是普天下最具有力量的，你所說的話不是讓你得勝有餘，就是讓你受到捆綁；許多人被自己的話語所擄獲，成為生活中的囚犯。若無神話語的同在，你將無法從祂的大能中得到信心。從神領受的屬靈力量，藉著說神的話，你將自己放在一個蒙受祝福的絶佳位置。神的創造權能一如太初時公義，當祂說要有光，就有光。祂的話在你的心裡孕育，在你的舌上成形，最後經由話語說出來，就變成屬靈的力量，釋放在你裡面的神的大能。人是按著神的形像被造的，學習像神一樣，說出祂滿有權能的話語，你將在每個處境中看到生命的改變；允許神的創造大能從你身上流瀉而出。<br>
		<strong>有聲出版及發行</strong>：磐石有聲事工（版權所有，請勿翻錄）

	<p class="is-size-6">
		<strong>關於作品</strong>：《荒漠甘泉》（Streams in the Desert）的作者是美國傳道人考門夫人，她與丈夫除了在美國服侍外，也曾一起在東亞傳教達二十年之久。這本書是她在丈夫重病時開始提筆所著，記錄了她的靈修生活，以及如何在苦難當中藉由禱告來獲得“盼望、忍耐與喜樂”。該書以日記形式書寫，其內文引言主要以《聖經》為依據，也引用了其他神學著作中的段句，並結合夫妻二人的見證和靈修生活，對《聖經》中某些經文展開了非常深入的闡釋，教導人們如何通過閱讀《聖經》聆聽神的話語並明白神的旨意。《荒漠甘泉》在1920年發行後，不但在美國發行量很高，更曾被譯為數十種文字，通行於全世界。<br>
